package com.onelpro.librelog.enums;

/**
 * Represents the possible types of broadcast stations.
 */
public enum StationType {
	/**
	 * Amplitude Modulation (AM) radio station.
	 */
	AM,

	/**
	 * Frequency Modulation (FM) radio station.
	 */
	FM,

	/**
	 * HD Radio (Hybrid Digital) station.
	 */
	HD_RADIO,

	/**
	 * Digital/Streaming station.
	 */
	DIGITAL,

	/**
	 * Satellite radio station.
	 */
	SATELLITE
}

